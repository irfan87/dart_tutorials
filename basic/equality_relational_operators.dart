// this is a simple application just to show the equality and relational operators in Dart
main() {
  int ageRestricted = 18;

  print(ageRestricted == 10);
  print(ageRestricted != 10);
  print(ageRestricted > 10);
  print(ageRestricted < 10);
  print(ageRestricted >= 10);
  print(ageRestricted <= 10);
}